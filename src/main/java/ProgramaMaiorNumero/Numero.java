package ProgramaMaiorNumero;

public class Numero {

    int valor_A;
    int valor_B;

    public Numero(int valor_A, int valor_B) {
        this.valor_A = valor_A;
        this.valor_B = valor_B;
    }

    public int getValor_A() {
        return valor_A;
    }

    public void setValor_A(int valor_A) {
        this.valor_A = valor_A;
    }

    public int getValor_B() {
        return valor_B;
    }

    public void setValor_B(int valor_B) {
        this.valor_B = valor_B;
    }
}
