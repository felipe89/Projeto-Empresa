package ProgramaNotas;

public class NotaAno {

    double notaA;
    double notaB;
    double notaC;

     public NotaAno(double notaA, double notaB, double notaC) {
        this.notaA = notaA;
        this.notaB = notaB;
        this.notaC = notaC;
    }

    public double getNotaA() {
        return notaA;
    }

    public void setNotaA(double notaA) {
        this.notaA = notaA;
    }

    public double getNotaB() {
        return notaB;
    }

    public void setNotaB(double notaB) {
        this.notaB = notaB;
    }

    public double getNotaC() {
        return notaC;
    }

    public void setNotaC(double notaC) {
        this.notaC = notaC;
    }
}
